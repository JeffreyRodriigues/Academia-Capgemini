
public class TipoDado {

	public static void main(String[] args) {
		// Demonstração de tipos de dados
		int numero;
		float decimalCurto;
		double decimalLongo;
		char caracter;
		String cadeiaCaracteres;
		
		numero = 10;
		decimalCurto = 10.5f;
		decimalLongo = 20.5;
		caracter = 'A';
		cadeiaCaracteres = "** Meu Curso de Java Fundamentos 2022 **";
		
		System.out.println(numero);
		System.out.println(decimalCurto);
		System.out.println(decimalLongo);
		System.out.println(caracter);
		System.out.println(cadeiaCaracteres);
	}

}
